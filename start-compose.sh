#!/usr/bin/env bash
# =============================================================================
# start-compose.sh — Alephant Docker Compose 部署脚本
# 使用方式:
#   cd alephant-docker/ && bash start-compose.sh
#
# 目录结构:
#   compose/   — docker-compose.yml + env 文件 + config/
#   sql/       — 共享 SQL（pgsql + clickhouse）
#
# 脚本自动完成:
# 1. 生成随机密码和密钥
# 2. 填充 compose/*.env 文件
# 3. 交互式收集 License 信息（邮箱 + license.jwt）
# 4. 启动 compose 栈 + 初始化数据库
# =============================================================================
set -euo pipefail

COMPOSE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
COMPOSE_PROJECT_DIR="${COMPOSE_DIR}/compose"
SQL_DIR="${COMPOSE_DIR}/sql"

# 颜色
GREEN='\033[0;32m'; BLUE='\033[0;34m'; YELLOW='\033[1;33m'; RED='\033[0;31m'; NC='\033[0m'
info()  { echo -e "${BLUE}[INFO]${NC}  $*"; }
ok()    { echo -e "${GREEN}[OK]${NC}    $*"; }
warn()  { echo -e "${YELLOW}[WARN]${NC}  $*"; }
err()   { echo -e "${RED}[ERROR]${NC} $*"; }

# ─── 密码/密钥生成 ──────────────────────────────────────────────────────────
gen_pswd() { openssl rand -base64 32 | tr -d '/+=' | cut -c1-24; }
gen_jwt()  { openssl rand -base64 48 | tr -d '/+='; }
gen_key()  { openssl rand -base64 32; }
gen_token(){ openssl rand -base64 32; }
gen_stripe(){ echo "sk_test_$(openssl rand -hex 32)"; }

# ─── 基础设施配置 ───────────────────────────────────────────────────────────
load_or_generate_infra_config() {
  if [[ -f "${COMPOSE_PROJECT_DIR}/infra.env" ]]; then
    info "复用已有 infra.env 中的基础设施凭证"
    # infra.env is generated by this script and contains shell-safe values.
    # shellcheck disable=SC1091
    source "${COMPOSE_PROJECT_DIR}/infra.env"
    # 兼容旧版 infra.env（缺少部分变量）
    POSTGRES_PASSWORD="${POSTGRES_PASSWORD:-"$(gen_pswd)"}"
    CLICKHOUSE_PASSWORD="${CLICKHOUSE_PASSWORD:-"$(gen_pswd)"}"
    VALKEY_PASSWORD="${VALKEY_PASSWORD:-"$(gen_pswd)"}"
    QDRANT_API_KEY="${QDRANT_API_KEY:-"$(gen_key)"}"
    MINIO_ROOT_USER="${MINIO_ROOT_USER:-minioadmin}"
    MINIO_ROOT_PASSWORD="${MINIO_ROOT_PASSWORD:-"$(gen_pswd)"}"
    return
  fi

  info "首次部署，生成基础设施凭证"
  POSTGRES_DB=alephant
  POSTGRES_USER=alephant
  POSTGRES_PASSWORD=$(gen_pswd)
  CLICKHOUSE_DB=alephant
  CLICKHOUSE_USER=default
  CLICKHOUSE_PASSWORD=$(gen_pswd)
  VALKEY_PASSWORD=$(gen_pswd)
  QDRANT_API_KEY=$(gen_token)
  MINIO_ROOT_USER=minioadmin
  MINIO_ROOT_PASSWORD=$(gen_pswd)

  cat > "${COMPOSE_PROJECT_DIR}/infra.env" << EOF
POSTGRES_DB=${POSTGRES_DB}
POSTGRES_USER=${POSTGRES_USER}
POSTGRES_PASSWORD=${POSTGRES_PASSWORD}
CLICKHOUSE_DB=${CLICKHOUSE_DB}
CLICKHOUSE_USER=${CLICKHOUSE_USER}
CLICKHOUSE_PASSWORD=${CLICKHOUSE_PASSWORD}
VALKEY_PASSWORD=${VALKEY_PASSWORD}
QDRANT_API_KEY=${QDRANT_API_KEY}
MINIO_ROOT_USER=${MINIO_ROOT_USER}
MINIO_ROOT_PASSWORD=${MINIO_ROOT_PASSWORD}
EOF
}

# ─── 前置检查 ────────────────────────────────────────────────────────────────
check_prereqs() {
  local FAIL=0
  command -v docker &>/dev/null || { err "Docker 未安装"; FAIL=1; }
  docker info &>/dev/null      || { err "Docker daemon 未运行"; FAIL=1; }
  command -v openssl &>/dev/null || { err "需要 openssl"; FAIL=1; }
  command -v curl &>/dev/null || command -v wget &>/dev/null || { err "需要 curl 或 wget"; FAIL=1; }
  [ -f "${COMPOSE_PROJECT_DIR}/docker-compose.yml" ] || { err "缺少 docker-compose.yml"; FAIL=1; }
  [ "$FAIL" = "1" ] && exit 1
  ok "前置检查通过"
}

# ─── 从 API 拉取最新镜像标签 ──────────────────────────────────────────────
fetch_latest_tags() {
  info "获取最新镜像标签..."
  local API_URL="https://image-versions.alephant.io/alephant/images/latest"
  local RESPONSE=""

  if command -v curl &>/dev/null; then
    RESPONSE=$(curl -sf --connect-timeout 5 "$API_URL" 2>/dev/null || true)
  elif command -v wget &>/dev/null; then
    RESPONSE=$(wget -qO- --timeout=5 "$API_URL" 2>/dev/null || true)
  fi

  if [ -z "$RESPONSE" ]; then
    warn "无法获取最新镜像标签，将使用 docker-compose.yml 中的默认值"
    return
  fi

  # API 返回完整镜像 URL: "saas-app": "registry.openmodels.link/alephant/alephant-app:20260713224933"
  local APP_IMAGE_VAL SAAS_SERVICE_IMAGE_VAL POLICY_SERVICE_IMAGE_VAL AI_GATEWAY_IMAGE_VAL LOGS_COLLECTOR_IMAGE_VAL

  APP_IMAGE_VAL=$(echo "$RESPONSE" | grep -o '"saas-app": *"[^"]*"' | cut -d'"' -f4 || true)
  SAAS_SERVICE_IMAGE_VAL=$(echo "$RESPONSE" | grep -o '"saas-service": *"[^"]*"' | cut -d'"' -f4 || true)
  POLICY_SERVICE_IMAGE_VAL=$(echo "$RESPONSE" | grep -o '"policy-service": *"[^"]*"' | cut -d'"' -f4 || true)
  AI_GATEWAY_IMAGE_VAL=$(echo "$RESPONSE" | grep -o '"ai-gateway": *"[^"]*"' | cut -d'"' -f4 || true)
  LOGS_COLLECTOR_IMAGE_VAL=$(echo "$RESPONSE" | grep -o '"logs-collector": *"[^"]*"' | cut -d'"' -f4 || true)

  # 导出完整镜像 URL — compose 文件中的 ${VAR:-default} 会自动使用
  if [ -n "$APP_IMAGE_VAL" ]; then
    export APP_IMAGE="${APP_IMAGE_VAL}"
  else
    warn "saas-app 标签获取失败，使用默认值"
  fi
  if [ -n "$SAAS_SERVICE_IMAGE_VAL" ]; then
    export SAAS_SERVICE_IMAGE="${SAAS_SERVICE_IMAGE_VAL}"
  else
    warn "saas-service 标签获取失败，使用默认值"
  fi
  if [ -n "$POLICY_SERVICE_IMAGE_VAL" ]; then
    export POLICY_SERVICE_IMAGE="${POLICY_SERVICE_IMAGE_VAL}"
  else
    warn "policy-service 标签获取失败，使用默认值"
  fi
  if [ -n "$AI_GATEWAY_IMAGE_VAL" ]; then
    export AI_GATEWAY_IMAGE="${AI_GATEWAY_IMAGE_VAL}"
  else
    warn "ai-gateway 标签获取失败，使用默认值"
  fi
  if [ -n "$LOGS_COLLECTOR_IMAGE_VAL" ]; then
    export LOGS_COLLECTOR_IMAGE="${LOGS_COLLECTOR_IMAGE_VAL}"
  else
    warn "logs-collector 标签获取失败，使用默认值"
  fi

  # 提取 tag 仅用于展示
  local TAG
  TAG="${APP_IMAGE_VAL##*:}"
  ok "镜像已更新: ${APP_IMAGE_VAL:-默认}"
  [ -n "$SAAS_SERVICE_IMAGE_VAL" ] && ok "          ${SAAS_SERVICE_IMAGE_VAL}"
  [ -n "$POLICY_SERVICE_IMAGE_VAL" ] && ok "          ${POLICY_SERVICE_IMAGE_VAL}"
  [ -n "$AI_GATEWAY_IMAGE_VAL" ] && ok "          ${AI_GATEWAY_IMAGE_VAL}"
  [ -n "$LOGS_COLLECTOR_IMAGE_VAL" ] && ok "          ${LOGS_COLLECTOR_IMAGE_VAL}"
}

