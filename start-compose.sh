# ─── License 激活信息收集 ────────────────────────────────────────────────
collect_license() {
  echo ""
  echo "═══════════════════════════════════════════"
  echo "  License 激活"
  echo "═══════════════════════════════════════════"
  echo ""

  # ── 1. 工作空间拥有者邮箱 ──
  while [ -z "${PRIVATE_WORKSPACE_OWNER_EMAILS:-}" ]; do
    read -r -p "  请输入工作空间拥有者邮箱 (多个用逗号分隔): " PRIVATE_WORKSPACE_OWNER_EMAILS
    [ -z "${PRIVATE_WORKSPACE_OWNER_EMAILS}" ] && warn "邮箱不能为空，请重新输入"
  done

  # ── 2. license.jwt ──
  local LICENSE_DIR="${COMPOSE_PROJECT_DIR}/license"
  mkdir -p "${LICENSE_DIR}"

  if [ -f "${LICENSE_DIR}/license.jwt" ] && [ -s "${LICENSE_DIR}/license.jwt" ]; then
    # 已有文件，询问是否覆盖
    local OVERWRITE
    read -r -p "  license/license.jwt 已存在，是否覆盖? (y/N): " OVERWRITE
    if [[ ! "${OVERWRITE}" =~ ^[Yy]$ ]]; then
      ok "保留已有 license.jwt"
    else
      write_license_file "${LICENSE_DIR}"
    fi
  else
    write_license_file "${LICENSE_DIR}"
  fi

  # ── 写入 .env ──
  echo "PRIVATE_WORKSPACE_OWNER_EMAILS=${PRIVATE_WORKSPACE_OWNER_EMAILS}" >> "${COMPOSE_PROJECT_DIR}/.env"
  ok "PRIVATE_WORKSPACE_OWNER_EMAILS 已写入 .env"

  echo ""
  ok "License 配置完成"
}

write_license_file() {
  local dir="$1"
  echo ""
  echo "  请将从 Alephant 团队获取的 JWT 内容粘贴到下方，"
  echo "  粘贴完成后按 Ctrl+D (EOF) 结束:"
  echo ""
  local content
  content=$(cat) || true
  if [ -n "${content}" ]; then
    echo "${content}" > "${dir}/license.jwt"
    ok "license.jwt 已写入 (${#content} 字符)"
  else
    warn "内容为空，跳过 license.jwt 写入"
  fi
}

# ─── 数据库初始化 ──────────────────────────────────────────────────────────
init_databases() {
  info "初始化数据库..."

  # ── 等待 PostgreSQL 就绪 ──
  info "  等待 PostgreSQL 就绪..."
  local RETRIES=30
  local WAIT=2
  local i=0
  while [ $i -lt $RETRIES ]; do
    if docker compose exec -T postgres pg_isready -U alephant &>/dev/null; then
      ok "  PostgreSQL 就绪"
      break
    fi
    i=$((i + 1))
    sleep $WAIT
  done
  if [ $i -ge $RETRIES ]; then
    err "PostgreSQL 未能就绪，跳过初始化"
    return 1
  fi

  # ── 检查是否已有表 ──
  local TABLE_COUNT
  TABLE_COUNT=$(docker compose exec -T postgres psql -U alephant -d alephant -t -c "SELECT count(*) FROM information_schema.tables WHERE table_schema='public';" 2>/dev/null | tr -d ' ')
  if [ -n "${TABLE_COUNT}" ] && [ "${TABLE_COUNT}" -gt 0 ] 2>/dev/null; then
    ok "  数据库已有 ${TABLE_COUNT} 张表，跳过初始化"
  else
    info "  导入 pgsql.sql..."
    docker compose exec -T postgres psql -U alephant -d alephant < "${SQL_DIR}/pgsql.sql" 2>&1 | grep -E "ERROR|CREATE TABLE|INSERT" || true
    ok "  pgsql.sql 导入完成"
  fi

  # ── 等待 ClickHouse 就绪 ──
  info "  等待 ClickHouse 就绪..."
  i=0
  while [ $i -lt $RETRIES ]; do
    if docker compose exec -T clickhouse clickhouse-client --user default --password "${CLICKHOUSE_PASSWORD}" --query "SELECT 1" &>/dev/null; then
      ok "  ClickHouse 就绪"
      break
    fi
    i=$((i + 1))
    sleep $WAIT
  done
  if [ $i -ge $RETRIES ]; then
    warn "ClickHouse 未能就绪，跳过初始化"
    return
  fi

  # ── ClickHouse 初始化（容忍已知的 Dictionary 引擎错误） ──
  local CH_TABLES
  CH_TABLES=$(docker compose exec -T clickhouse clickhouse-client --user default --password "${CLICKHOUSE_PASSWORD}" --query "SHOW TABLES" 2>/dev/null | wc -l | tr -d ' ')
  if [ -n "${CH_TABLES}" ] && [ "${CH_TABLES}" -gt 0 ] 2>/dev/null; then
    ok "  ClickHouse 已有 ${CH_TABLES} 张表，跳过初始化"
  else
    info "  导入 clickhouse.sql..."
    # 已知 Dictionary 引擎表创建会报错，忽略该错误
    docker compose exec -T clickhouse clickhouse-client --user default --password "${CLICKHOUSE_PASSWORD}" < "${SQL_DIR}/clickhouse.sql" 2>&1 | grep -v "DB::Exception" || true
    ok "  clickhouse.sql 导入完成"
  fi
}

# ─── 启动服务 ──────────────────────────────────────────────────────────────
start_services() {
  info "启动所有服务..."
  cd "${COMPOSE_PROJECT_DIR}"
  docker compose up -d 2>&1
  ok "服务已启动!"
  echo ""
  echo "  查看状态: docker compose ps"
  echo "  查看日志: docker compose logs -f"
}

